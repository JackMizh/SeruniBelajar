package com.serunibelajar.app;

public class Notification {
    public String id_notif, title_notif, subtitle_notif;

    public Notification() {
    }

    public String getId_notif() {
        return id_notif;
    }

    public void setId_notif(String id_notif) {
        this.id_notif = id_notif;
    }

    public String getTitle_notif() {
        return title_notif;
    }

    public void setTitle_notif(String title_notif) {
        this.title_notif = title_notif;
    }

    public String getSubtitle_notif() {
        return subtitle_notif;
    }

    public void setSubtitle_notif(String subtitle_notif) {
        this.subtitle_notif = subtitle_notif;
    }
}
