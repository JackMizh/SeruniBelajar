package com.serunibelajar.app;

public class Jurusan {
    String id_jurusan, nama_jurusan, kepala_jurusan, sekolah_jurusan;

    public Jurusan() {
    }

    public String getId_jurusan() {
        return id_jurusan;
    }

    public void setId_jurusan(String id_jurusan) {
        this.id_jurusan = id_jurusan;
    }

    public String getNama_jurusan() {
        return nama_jurusan;
    }

    public void setNama_jurusan(String nama_jurusan) {
        this.nama_jurusan = nama_jurusan;
    }

    public String getKepala_jurusan() {
        return kepala_jurusan;
    }

    public void setKepala_jurusan(String kepala_jurusan) {
        this.kepala_jurusan = kepala_jurusan;
    }

    public String getSekolah_jurusan() {
        return sekolah_jurusan;
    }

    public void setSekolah_jurusan(String sekolah_jurusan) {
        this.sekolah_jurusan = sekolah_jurusan;
    }
}
