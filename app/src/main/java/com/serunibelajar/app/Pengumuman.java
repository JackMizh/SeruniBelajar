package com.serunibelajar.app;

public class Pengumuman {
    public String id_pengumuman, title_pengumuman, subtitle_pengumuman, foto_pengumuman;

    public Pengumuman() {
    }

    public String getId_pengumuman() {
        return id_pengumuman;
    }

    public void setId_pengumuman(String id_pengumuman) {
        this.id_pengumuman = id_pengumuman;
    }

    public String getTitle_pengumuman() {
        return title_pengumuman;
    }

    public void setTitle_pengumuman(String title_pengumuman) {
        this.title_pengumuman = title_pengumuman;
    }

    public String getSubtitle_pengumuman() {
        return subtitle_pengumuman;
    }

    public void setSubtitle_pengumuman(String subtitle_pengumuman) {
        this.subtitle_pengumuman = subtitle_pengumuman;
    }

    public String getFoto_pengumuman() {
        return foto_pengumuman;
    }

    public void setFoto_pengumuman(String foto_pengumuman) {
        this.foto_pengumuman = foto_pengumuman;
    }
}
