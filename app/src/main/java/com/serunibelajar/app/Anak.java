package com.serunibelajar.app;

public class Anak {
    public String nama_siswa, nama_sekolah, id_sekolah, foto_siswa;

    public Anak() {
    }

    public String getNama_siswa() {
        return nama_siswa;
    }

    public void setNama_siswa(String nama_siswa) {
        this.nama_siswa = nama_siswa;
    }

    public String getNama_sekolah() {
        return nama_sekolah;
    }

    public void setNama_sekolah(String nama_sekolah) {
        this.nama_sekolah = nama_sekolah;
    }

    public String getId_sekolah() {
        return id_sekolah;
    }

    public void setId_sekolah(String id_sekolah) {
        this.id_sekolah = id_sekolah;
    }

    public String getFoto_siswa() {
        return foto_siswa;
    }

    public void setFoto_siswa(String foto_siswa) {
        this.foto_siswa = foto_siswa;
    }
}
